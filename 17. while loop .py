# 1-100 number print using while loop
'''
i=1
while i <=100 :
  print(i)
  i = i + 1
print('While loop end.')
'''

# 1-100 summation
'''
j = 1; sum =0
while j <= 100 :
  sum = sum + j; j = j + 1

print('While loop end.')
print('Sum is : ',sum)
'''

# 2+4+6+........+100 summation
